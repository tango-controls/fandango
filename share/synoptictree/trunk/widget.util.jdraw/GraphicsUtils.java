  static BasicStroke createStrokeForLine(int lw, int style) {

    BasicStroke bs = null;

    if (lw != 1 || style != JDObject.LINE_STYLE_SOLID) {
      switch (style) {
        case JDObject.LINE_STYLE_DOT:
          bs = new BasicStroke(lw, BasicStroke.CAP_BUTT, BasicStroke.JOIN_MITER, 10.0f, dotPattern, 0.0f);
          break;
        case JDObject.LINE_STYLE_DASH:
          bs = new BasicStroke(lw, BasicStroke.CAP_BUTT, BasicStroke.JOIN_MITER, 10.0f, dashPattern, 0.0f);
          break;
        case JDObject.LINE_STYLE_LONG_DASH:
          bs = new BasicStroke(lw, BasicStroke.CAP_BUTT, BasicStroke.JOIN_MITER, 10.0f, longDashPattern, 0.0f);
          break;
        case JDObject.LINE_STYLE_DASH_DOT:
          bs = new BasicStroke(lw, BasicStroke.CAP_BUTT, BasicStroke.JOIN_MITER, 10.0f, dashDotPattern, 0.0f);
          break;
        default:
          bs = new BasicStroke(lw);
          break;
      }
    }

    return bs;
  }

  private static void fillPattern(int[] p,Color c) {
    int rgb=c.getRGB() & 0x00FFFFFF;
    for(int i=0;i<p.length;i++) {
      p[i] &= 0xFF000000;
      p[i] |= rgb;
    }
  }

  private static void shitPattern(int[] p) {
    for(int i=0;i<p.length;i++) p[i] = p[i] << 24;
  }

